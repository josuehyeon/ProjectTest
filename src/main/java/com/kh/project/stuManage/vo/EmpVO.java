package com.kh.project.stuManage.vo;

public class EmpVO {
	private int empNo;
	private String empName;
	private int empClass;
	private String empDept;
	private int empColl;
	private int empJob;
	public int getEmpNo() {
		return empNo;
	}
	public void setEmpNo(int empNo) {
		this.empNo = empNo;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public int getEmpClass() {
		return empClass;
	}
	public void setEmpClass(int empClass) {
		this.empClass = empClass;
	}
	public String getEmpDept() {
		return empDept;
	}
	public void setEmpDept(String empDept) {
		this.empDept = empDept;
	}
	public int getEmpColl() {
		return empColl;
	}
	public void setEmpColl(int empColl) {
		this.empColl = empColl;
	}
	public int getEmpJob() {
		return empJob;
	}
	public void setEmpJob(int empJob) {
		this.empJob = empJob;
	}
	
	

}
